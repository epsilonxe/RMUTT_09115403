x = 'Hello'
y = 'Hello2021'
z = '20210804'

print('x is alphabet? ', x.isalpha())
print('y is alphabet? ', y.isalpha())
print('z is alphabet? ', z.isalpha())

print('x is digits? ', x.isdigit())
print('y is digits? ', y.isdigit())
print('z is digits? ', z.isdigit())

print('x is alphabet/digits? ', x.isalnum())
print('y is alphabet/digits? ', y.isalnum())
print('z is alphabet/digits? ', z.isalnum())